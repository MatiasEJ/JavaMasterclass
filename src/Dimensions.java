
public class Dimensions {
	private int width;
	private int height;
	private int deep;
	public Dimensions(int width, int height, int deep) {
		this.width = width;
		this.height = height;
		this.deep = deep;
	}
	public int getWidth() {
		return width;
	}
	public int getHeight() {
		return height;
	}
	public int getDeep() {
		return deep;
	}
	
	
	
}
